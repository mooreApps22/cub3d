#include "ft_printf.h"

int	ft_printf_s(char *str)
{
	unsigned int	count;

	if (!str)
	{
		ft_putstr_fd("(null)", 1);
		return (6);
	}
	ft_putstr_fd(str, 1);
	count = ft_strlen(str);
	return (count);
}
